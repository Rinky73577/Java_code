public class Arithmetic {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        // challenge

        int x = 4;
        int y = 6;

        System.out.println("\n" + x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

        // produvt of two floating point number

        float no1 = 5.2f;
        float no2 = 3.1f;

        System.out.println("\n" + no1 * no2);

    }
}
